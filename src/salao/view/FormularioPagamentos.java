    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        double numerario, cartao, cheque, totalVenda, troco, totalPagamento;
        numerario = Double.valueOf(txtNumerario.getText());
        cartao = Double.valueOf(txtCartao.getText());
        cheque = Double.valueOf(txtCheque.getText());
        totalVenda = Double.valueOf(txtTotalVenda.getText());
        totalPagamento = numerario+cartao+cheque; //Inicialização da variável totalPagamento.
        troco = totalPagamento - totalVenda; //Inicialização da variável troco.
        txtTroco.setText(String.valueOf(troco));
        //Condição para efectur o pagamento.
        if(totalPagamento>=totalVenda){
            Vendas v = new Vendas();
            //Objectos para a gravação na tabela de vendas.
            v.setClientes(clientes); //Pegando o ID do cliente.
            Date agora = new Date(); //Obtendo a data do sistema.
            SimpleDateFormat dataEUA = new SimpleDateFormat("YYYY-MM-dd HH:mm:ss"); //Salvando a data obtida no formato Americano.
            String dataMysql = dataEUA.format(agora); //Mudando o nome para Mysql 
            v.setData(dataMysql); //Salvando o valor armazenda no formato acima.
            v.setTotalVenda(totalVenda); //Recebendo o dado do campo txtTotalVenda.
            v.setObservacoes(txtObservacoes.getText()); //Recebendo o dado do campo Observações.
            VendasDAO vd = new VendasDAO(); //Chamando a classe VendasDAO.
            vd.Salvar(v); //Método para salvar a venda.
            v.setId(vd.retornaUltimoIDVenda()); //Método para retornar a última venda.
//            JOptionPane.showMessageDialog(null, "ID da última venda!"+v.getId()); //Método para testar o retorno da última venda.
            
            //Salvando os itens do carrinho no formulário das vendas.
            for(int i=0; i<meusProdutos.getRowCount(); i++){
                int qtdComprada, qtdActualizada;
                Servicos s = new Servicos(); //Instanciando a classe produtos
                ServicosDAO pd = new ServicosDAO(); //Instanciando a classe produtosDAO
                ItensVendas item = new ItensVendas();
                item.setVendas(v); //Recebendo os itens da venda.
                s.setId(Integer.valueOf(meusProdutos.getValueAt(i, 0).toString())); //Pegando o 1º iten da tabela caarrinhos
                item.setServicos(s); //Dizer para o sistema que o item recebe da classe produtos
                item.setQtd(Integer.valueOf(meusProdutos.getValueAt(i, 2).toString())); //Pegando o 2º iten da tabela caarrinhos
                item.setSubtotal(Double.valueOf(meusProdutos.getValueAt(i, 4).toString())); //Pegando o 2º iten da tabela caarrinhos
                qtdComprada = Integer.valueOf(meusProdutos.getValueAt(i, 2).toString());
                ItensVendasDAO ivd = new ItensVendasDAO();
                ivd.Salvar(item); //Salvar o item  
                dispose();
                FormularioVendas fv = new FormularioVendas();
                fv.setVisible(true);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possível fazer a venda!\n O valor pago é menor que o valor da venda.");
        }
        
    }//GEN-LAST:event_btnPagarActionPerformed

